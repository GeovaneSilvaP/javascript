//switch

const job = "Programador"

switch (job) {
  case 'Advogado':
    console.log("Você é Advogado")
    break;
  
  case 'Professor':
    console.log("Você é Professor")
    break;
  case 'Programador':
    console.log("Você é Programador")
    break;
  
  default:
    console.log("Profissão não encontrada")
}